<p><strong>Sprezzatura</strong> is het debuutalbum van Bert van Stam. De titel van dit album is een Italiaanse term waarmee kunst aangeduid wordt die hoogstaand is, maar tegelijk natuurlijk en moeiteloos overkomt. Dit is ook van toepassing op de muziek op dit album: verfijnde virtuositeit!</p>
<p>Op het album staat muziek van Johann Sebastian Bach, Wolfgang Amadeus Mozart, Michael Radulescu, Michael Schütz en eigen werk van Bert van Stam.</p>
<p>De muziek is opgenomen op 17 en 18 oktober 2018 op het Mense Ruiter-orgel (1952, 23/IIP) in de Maranathakerk Den Haag.</p>
