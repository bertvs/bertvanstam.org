<p><strong>La Belle Époque</strong> is het tweede album van Bert van Stam. Deze cd bevat een staalkaart van orgelmuziek uit de periode 1890-1914, en is opgenomen op het Franssen-orgel (1906) in de Elandstraatkerk in Den Haag.</p>
<p>Op het album staat muziek van César Franck, Erik Satie, Louis Vierne, Edward Bairstow, William Wolstenholme, Max Reger, Charles Ives, Nadia Boulanger en Léon Boëllmann.</p>
