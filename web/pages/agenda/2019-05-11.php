<h3>Toelichting</h3>
<p><img src="/signatures.jpg" alt="muziekvoorbeeld" /></p>
<p>Omdat muzieknoten aangeduid worden met letters, is het mogelijk namen in muziek om te zetten. Dit wordt een ‘muzikale handtekening’ genoemd.</p>
<h4>Franz Liszt &#8211; Präludium und Fuge über B.A.C.H.</h4>
<p>De bekendste hiervan is het BACH-motief, door veel componisten gebruikt. De virtuoze preludium en fuga die Franz Liszt over dit thema schreef, is een monumentaal hoogtepunt uit de orgelmuziek.</p>
<h4>Johann Sebastian Bach &#8211; Adagio uit Sonate voor viool en klavecimbel no. 4 (orgelbewerking Franz Liszt)</h4>
<p>Liszt eerde Bach niet alleen met deze preludium en fuga, maar schreef ook verschillende bewerkingen van Bachs muziek voor piano en orgel, waaronder het prachtige Adagio uit de vierde vioolsonate.</p>
<h4>Dimitri Sjostakovitsj &#8211; Allegretto uit Symfonie no. 10 (orgelbewerking Bert van Stam)</h4>
<p>In het derde deel uit de 10<sup>e</sup> symfonie van Sjostakovitsj zijn de thema’s niet te missen. Het DSCH-motief (als afkorting van de eigen naam van de componist) komt als eerste aan bod.</p>
<p>Het tweede thema verwijst naar Elmira, de voornaam van een studente op wie de componist verliefd werd. Beide thema’s worden afgewisseld en gecombineerd op verschillende dansvormen.</p>
<h4>Jehan Alain &#8211; Deux danses à Agni Yavishta</h4>
<p>De ‘Deux danses’ van Jehan Alain zijn een goed voorbeeld van de inventiviteit en virtuositeit van deze organist en componist. De twee korte dansen zijn geïnspireerd op oosterse muziek: ‘Agni’ is een Hindu-god van het vuur, en ‘Yavishta’ betekent ‘de jeugdige’.</p>
<h4>Maurice Duruflé &#8211; Prélude et fugue sur le nom d’Alain</h4>
<p>Alain overleed op tragische wijze op 29-jarige leeftijd. Als eerbetoon aan hem schreef Maurice Duruflé de indrukwekkende prelude en fuga, waarvan het thema ontleend is aan de naam van Alain. De kenner hoort in de prelude ook nog een citaat van de ‘Litanies’, het bekendste orgelwerk van Alain, voorbijkomen.</p>
		

