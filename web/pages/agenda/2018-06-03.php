<h3>Toelichting</h3>
<p>Vanmiddag bent u toehoorder van een divers programma: van Bachs eeuwenoude muziek tot kersverse improvisaties en van Schuurman tot Schütz. Al deze muziek heeft echter in ieder geval één ding gemeenschappelijk, namelijk dat het een plaats in een kerkdienst zou kunnen hebben.</p>
<p>Vorige week was het zondag Trinitatis, waarop de Drieëenheid van God centraal staat. Voor die zondag schreef ik mijn Sonate no. 1 voor orgel. Hierin staat de melodie van ‘Heilig, heilig, heilig’ centraal. Deze melodie begint met een stijgende drieklank: drie afzonderlijke tonen, die samen één akkoord vormen. De sonate bestaat uit drie delen, met als subtitels ‘Unity’, ‘Trinity’ en ‘Infinity’. U krijgt een voorproefje met het eerste deel; nog nooit eerder tijdens een concert uitgevoerd!</p>
<p>Eén van mijn voorgangers als organist van de Maranathakerk is Adriaan C. Schuurman. Hij schreef veel prachtige kerkmuziek. Een partita is in de orgelmuziek een serie variaties over een lied. Deze Partita over psalm 130 is waarschijnlijk ontstaan in de tijd dat hij in de Maranathakerk organist was. Aan het eind van dit programma speel ik zelf een partita als improvisatie: het is een vorm die erg geschikt is tijdens bijvoorbeeld de inzameling in een kerkdienst.</p>
<p>Het schrijven van transcripties is voor mij een natuurlijk onderdeel van het werken als musicus. Het is leerzaam en verrijkend om muziek van anderen voor je eigen instrument te bewerken om het vervolgens zelf te kunnen uitvoeren. Zo deed ik dit met het bekende Klarinetkwintet van Mozart, om deze prachtige melodieën op orgel te laten klinken. En zo deed Liszt dit met het Adagio, oorspronkelijk voor viool en klavecimbel; een juweeltje uit het oeuvre van Bach en prachtig omgezet voor orgel.</p>
<p>De enorme rijkdom aan orgelmuziek uit het verleden biedt ons voldoende gelegenheid het daarbij te houden. Toch zoek ik zelf graag de verbinding met de hedendaagse muziek. Ook in de kerkmuziek wordt er tegenwoordig veel met lichte muziek gedaan. Schütz is een autoriteit op het gebied van de geschiedenis van popmuziek èn organist, en hij schreef deze Pop-Stücke die zowel goed klinken als goed in elkaar zitten.</p>
<p>Het programma sluit af waar het begon: rond Trinitatis. Bach schreef zijn ‘Es-dur’, zoals deze Preludium en Fuga onder organisten heet, over dit thema. De Preludium is met zijn grootse opzet de langste die Bach geschreven heeft. Zowel de Preludium als de Fuga zijn opgebouwd uit drie thema’s, die op ingenieuze manier afgewisseld en met elkaar gecombineerd worden. Het eerste thema in de Preludium heeft de grandeur van een Franse ouverture. Het tweede thema is lichter van opzet en bestaat uit korte akkoorden en sierlijk slingerende melodie, in de galante stijl. Het derde thema is fugatisch van opzet en is opgebouwd uit dalende toonladders. Er is niet veel fantasie voor nodig om hierin de karakters van Vader, Zoon en Geest te horen. De fuga begint met het eerste fugathema in lange noten. Het tweede en derde fugathema doen er elk hun eigen schepje bovenop in intensiteit en beweeglijkheid, terwijl deze beiden op sublieme wijze gecombineerd worden met het eerste fugathema.</p>

