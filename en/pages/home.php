<p>Music is music, regardless the genre, and that has fascinated Bert van Stam (1989) all his life. He is head organist of the <a href="https://maranathakerkdenhaag.nl/" target="_blank" rel="noopener noreferrer">Maranatha Church</a> in The Hague. He enjoys playing the very best organ music by great classical composers such as Bach, Mozart, Liszt, Franck and Messiaen, but also enjoys working with living composers for new, exciting music. He also composes and makes arrangements of piano music, large orchestral works, jazz and pop songs for organ.</p>
