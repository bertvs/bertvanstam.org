<p><strong>La Belle Époque</strong> is Bert van Stam's second album. This cd contains exemplary organ music from the period between 1890-1914. The music is recorded on the Franssen-organ (1906) in the Elandstraatkerk in The Hague.</p>
<p>The album contains music by César Franck, Erik Satie, Louis Vierne, Edward Bairstow, William Wolstenholme, Max Reger, Charles Ives, Nadia Boulanger and Léon Boëllmann.</p>
